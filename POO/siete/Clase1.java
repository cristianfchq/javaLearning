package POO.siete;

public class Clase1 {

    private int edad;
    private String nombre;

    //metodo setter para establecer
    public void setEdad(int edad){
        this.edad =  edad;
    }

    //metodo getter para obtener
    public int getEdad(){
        return this.edad;
    }
    
    //metodo setter para establecer
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    //metodo setter para obtener
    public String getNombre() {
        return this.nombre;
    }

}
